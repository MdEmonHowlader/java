package test4;

public class ArrayString {
    public static void main(String[] args) {
        int[] num={2,3,6,5};
        String[] name={"Emon, limon, emon1"};
        for(int i=0; i<num.length; i++){
            System.out.println(num[i]);
        }
        for(int j=0; j<name.length; j++){
            System.out.println(name[j]);
        }
    }
}
